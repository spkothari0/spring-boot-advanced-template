    @Override
    public User loadUserByUsername(String username) throws UsernameNotFoundException {
        Optional<User> u = userRepo.findByUsername(username);
        if (u.isEmpty()) {
            throw new UsernameNotFoundException("User with username: " + username + " not found!");
        }
        return u.get();
    }

    public UserBean loadUserByEmail(String email) throws UsernameNotFoundException {
        Optional<User> u = userRepo.findByEmail(email);
        if (u.isEmpty()) {
            throw new UsernameNotFoundException(email);
        }
        return GenericBeanMapper.map(u, UserBean.class);
    }

    public UserBean createUser(UserBean userBean) {
        if (userRepo.findByUsername(userBean.getUsername()).isPresent() || userRepo.findByEmail(userBean.getEmail()).isPresent())
            throw new IllegalStateException("User already exists. Please update your username and email");

        return saveUser(userBean);
    }

    public UserBean updateUser(UserBean userBean) {
        boolean doesExists = userRepo.existsById(userBean.getId());
        if (doesExists) {
            return saveUser(userBean);
        }
        return null;
    }

    public Boolean deleteUser(UUID id) {
        boolean doesExists = userRepo.existsById(id);
        if (doesExists) {
            userRepo.deleteById(id);
            return true;
        }
        return false;
    }

    public String verifyUser(String token) {
        Date currentTime = new Date();
        boolean isExpired = currentTime.after(jwtUtils.getExpirationDateFromToken(token));
        String username = jwtUtils.getUsernameFromToken(token);
        if (username == null || isExpired) {
            return verificationFailed();
        }
        Optional<User> u = userRepo.findByUsername(username);
        if (u.isEmpty()) {
            throw new UsernameNotFoundException("User with username: " + username + " not found!");
        }
        User user = u.get();
        user.setIsLocked(false);

        return verificationSuccess(user.getFirstName());

    }

    public boolean uploadProfileImage(String username, MultipartFile file) {
        Optional<User> u = userRepo.findByUsername(username);
        if (u.isEmpty()) {
            throw new UsernameNotFoundException("User with username: " + username + " not found!");
        }
        User user = u.get();
        if (!AppConstant.AWSServiceEnabled()) {
            log.warn("AWS services are disabled ! Please enable them to continue.");
            return false;
        }
        String path = Constants.AWSS3ProfileImagePrefix + username + "/";
        boolean fileName = storageService.saveFile(path, file.getName(), file);
        if (fileName) {
            user.setProfileImageName(path + file.getName());
            userRepo.save(user);
            return true;
        } else {
            return false;
        }
    }

    private UserBean saveUser(UserBean userBean) {

        User user = GenericBeanMapper.map(userBean, User.class);

        sendVerificationMail(user);

        user.setPassword(passwordEncoder.encode(userBean.getPassword()));

        // Fetch Role entity based on role name
        Role role = roleRepo.findByRoleName(RoleType.valueOf(userBean.getRole()))
                .orElseThrow(() -> new IllegalArgumentException("Invalid role: " + userBean.getRole()));
        user.setRole(role);

        user = userRepo.save(user);
        return GenericBeanMapper.map(user, UserBean.class);
    }

    private void sendVerificationMail(User user) {
        String link = Constants.ApplicationURL + Constants.VerificationAPI + jwtUtils.generateTokenFromUsername(user);
        emailService.sendEmail(user.getEmail(), Constants.ApplicationName + " Account Verification Email", buildEmail(user.getFirstName(), link));
    }

